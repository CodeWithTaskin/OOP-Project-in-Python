class Calculator:
    def __init__(self):
        self.__value = 0

    def addition(self,x,y):
        return x + y
    
    def subtraction(self,x,y):
        return x - y
    
    def multiplication(self,x,y):
        return x * y
    
    def division(self,x,y):
        return x / y
    
    def reminder(self,x,y):
        return x % y
